#include <stdio.h>
#include "pico/stdlib.h"
#include "hardware/gpio.h"

const int BTN1 = 26;
const int BTN2 = 7;

int main() {
    stdio_init_all();

    gpio_init(BTN1);
    gpio_set_dir(BTN1, GPIO_IN);
    gpio_pull_up(BTN1);

    gpio_init(BTN2);
    gpio_set_dir(BTN2, GPIO_IN);
    gpio_pull_up(BTN2);

    int cnt_1 = 1;
    int cnt_2 = 1;

    while (true) {
        if (!gpio_get(BTN1)) {
            sleep_ms(30);
            if (!gpio_get(BTN1)) {
                printf("Botao 1: %d\n", cnt_1++);
                while (!gpio_get(BTN1)) {
                    sleep_ms(30);
                }
            }
        }

        if (!gpio_get(BTN2)) {
            sleep_ms(30);
            if (!gpio_get(BTN2)) {
                printf("Botao 2: %d\n", cnt_2++);
                while (!gpio_get(BTN2)) {
                    sleep_ms(30);
                }
            }
        }
    }
}
